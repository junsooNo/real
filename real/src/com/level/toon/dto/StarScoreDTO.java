package com.level.toon.dto;

public class StarScoreDTO {
	private int toon_num;
	private int ep_num;
	private int member_num;
	private int star_score;
	
	public int getToon_num() {
		return toon_num;
	}
	public void setToon_num(int toon_num) {
		this.toon_num = toon_num;
	}
	public int getEp_num() {
		return ep_num;
	}
	public void setEp_num(int ep_num) {
		this.ep_num = ep_num;
	}
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
	public int getStar_score() {
		return star_score;
	}
	public void setStar_score(int star_score) {
		this.star_score = star_score;
	}	
}
